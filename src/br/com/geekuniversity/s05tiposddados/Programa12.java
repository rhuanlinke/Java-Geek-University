package br.com.geekuniversity.s05tiposddados;
//Tipos de dados
//Numericos (Reais)
public class Programa12 {

	public static void main(String[] args) {
		//Tipos primários/primitivos:
		//Por padrão os numeros reais em Java são considerados double
		double preco1 = 23.4;
		float preco2 = 4.1f; //Realf
		
		//Tipos não primários/primitivos
		Float preco3 = 44.5f;
		Double preco4 = 44.5;
		
		System.out.println(preco1);
		System.out.println(preco2);
		System.out.println(preco3);
		System.out.println(preco4);
		
	}

}
